static uint32_t asahi_sl_opid_value = 264;


int execute_module_asahi(FILE *fd_out, uint8_t *data_p, size_t size)
{
	int res;
	struct rule_verifier *rule_handle = NULL;
	struct dev_binding binding = {};

	if (0 == strncmp("asahi:simlock", (char *)data_p, size)) {

		res = rule_init(&rule_handle,
			&binding,
			RULE_USAGE_SIMLOCK);
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to init rule session\n");
			goto testcase_error;
		}

		res = asahi_verify(
				rule_handle,
				(uint8_t *)s1_root_cert_test,
				sizeof(s1_root_cert_test),
				asahi_sl,
				sizeof(asahi_sl),
				asahi_sl_hash,
				sizeof(asahi_sl_hash));
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to verify asahi signature: (%d)\n",
					res);
			goto testcase_error;
		}

		rule_deinit(rule_handle);

		fprintf(fd_out, "OKAY");
	} else if (0 == strncmp("asahi:unexpected-simlock-opid",
			(char *)data_p, size)) {

		res = rule_init(&rule_handle,
			&binding,
			RULE_USAGE_SIMLOCK);
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to init rule session\n");
			goto testcase_error;
		}

		res = asahi_verify(
				rule_handle,
				(uint8_t *)s1_root_cert_test,
				sizeof(s1_root_cert_test),
				asahi_sl_opid,
				sizeof(asahi_sl_opid),
				asahi_sl_opid_hash,
				sizeof(asahi_sl_opid_hash));
		if (res != -FCSERR_EOPID) {
			FCS_LOG_ERR(
				"Unexpected OPID should fail verify\n");
			goto testcase_error;
		}

		rule_deinit(rule_handle);

		fprintf(fd_out, "OKAY");
	} else if (0 == strncmp("asahi:expected-simlock-opid",
			(char *)data_p, size)) {

		res = rule_init(&rule_handle,
			&binding,
			RULE_USAGE_SIMLOCK);
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to init rule session\n");
			goto testcase_error;
		}

		res = rule_set_opid(rule_handle, asahi_sl_opid_value);
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to init rule session\n");
			goto testcase_error;
		}

		res = asahi_verify(
				rule_handle,
				(uint8_t *)s1_root_cert_test,
				sizeof(s1_root_cert_test),
				asahi_sl_opid,
				sizeof(asahi_sl_opid),
				asahi_sl_opid_hash,
				sizeof(asahi_sl_opid_hash));
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to verify asahi signature: (%d)\n",
					res);
			goto testcase_error;
		}

		rule_deinit(rule_handle);

		fprintf(fd_out, "OKAY");
	} else if (0 == strncmp("asahi:mismatched-simlock-opid",
			(char *)data_p, size)) {

		res = rule_init(&rule_handle,
			&binding,
			RULE_USAGE_SIMLOCK);
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to init rule session\n");
			goto testcase_error;
		}

		res = rule_set_opid(rule_handle, asahi_sl_opid_value+1);
		if (res != FCSERR_OK) {
			FCS_LOG_ERR("Failed to init rule session\n");
			goto testcase_error;
		}

		res = asahi_verify(
				rule_handle,
				(uint8_t *)s1_root_cert_test,
				sizeof(s1_root_cert_test),
				asahi_sl_opid,
				sizeof(asahi_sl_opid),
				asahi_sl_opid_hash,
				sizeof(asahi_sl_opid_hash));
		if (res != -FCSERR_EOPID) {
			FCS_LOG_ERR(
				"Mismatched OPID should fail verify\n");
			goto testcase_error;
		}

		rule_deinit(rule_handle);

		fprintf(fd_out, "OKAY");
	} else {
		fprintf(fd_out, "ERRORunknown testcase:%s", data_p);
	}
	return FCSERR_OK;

testcase_error:
	fprintf(fd_out, "ERRORres:%d", res);
	rule_deinit(rule_handle);
	return FCSERR_OK;
}
