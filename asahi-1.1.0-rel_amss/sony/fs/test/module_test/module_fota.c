int execute_module_fota(FILE *fd_out, uint8_t *data_p, size_t size)
{
	int res;

	if (0 == strncmp("fota:normal", (char *)data_p, size)) {
		res = sinfota_verify(
			(uint8_t *)s1_root_cert_test,
			sizeof(s1_root_cert_test),
			fota_normal_buf,
			sizeof(fota_normal_buf),
			fota_normal_digest_list,
			sizeof(fota_normal_digest_list)/
				sizeof(struct sha256_list_item));
		if (FCSERR_OK == res)
			fprintf(fd_out, "OKAY");
		else
			fprintf(fd_out, "ERRORres:%d", res);
	} else if (0 == strncmp("fota:wrongcert", (char *)data_p, size)) {
		res = sinfota_verify(
			(uint8_t *)s1_root_cert_test,
			sizeof(s1_root_cert_test),
			fota_wrongcert_buf,
			sizeof(fota_wrongcert_buf),
			fota_normal_digest_list,
			sizeof(fota_normal_digest_list)/
				sizeof(struct sha256_list_item));
		if (-FCSERR_EVCC == res)
			fprintf(fd_out, "OKAY");
		else
			fprintf(fd_out, "ERRORres:%d", res);
	} else if (0 == strncmp("fota:wronghash", (char *)data_p, size)) {
		res = sinfota_verify(
			(uint8_t *)s1_root_cert_test,
			sizeof(s1_root_cert_test),
			fota_normal_buf,
			sizeof(fota_normal_buf),
			fota_wrong_digest_list,
			sizeof(fota_wrong_digest_list)/
				sizeof(struct sha256_list_item));
		if (-FCSERR_EVHSH == res)
			fprintf(fd_out, "OKAY");
		else
			fprintf(fd_out, "ERRORres:%d", res);
	} else {
		fprintf(fd_out, "ERRORunknown testcase:%s", data_p);
	}
	return FCSERR_OK;
}


